package com.syntax.class07;

public class WhileLoopIntro {

	public static void main(String[] args) {
		 
		int num=50;
		
		while(num>=1) {
			System.out.println(num);
			num--;
		}
		System.out.println("_________________________________________");
		
		int num1=1;
		
		while(num1<=20) {
			System.out.println(num1);
			num1+=2;
		}

	}

}
