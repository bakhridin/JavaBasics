package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		// 8 primitive datatypes
		//to store whole numbers--> byte, short, int, long
		
		byte variableOne=10;
		
		short variable2=100;
		
		int variable3=1000;
		int i=2000;
		int aa=3000;
		
		long variable4=10000000;
		
		//to store decimal numbers-->float, double
		
		float variable5=12.99f;
		
		double anyName= 132.89;
		
		//to store single charater--> char
		char singleValue='F';
		char anotherVariable='%';
		//to store boolean values-->
		
		boolean hungry=true;
		boolean thirsty=false;
		System.out.println(anyName+i);
		
	}

}
