package com.syntax.class01;

public class PracticeVariables {

	public static void main(String[] args) {
		// Practice Variables 
		
		// Whole Number
		
		byte avaJ=122;
		
		short avaJ1=3355;
		
		int   avaJ2=3334445;
		
		long  avaJ3=10000000l;
		
		// Decimal number
		
		float avaJ4=12.21f;
		double avaJ5=111.2222;
		
		//single character
		
		char avaJ6='@';
		char avaJ7='$';
		
		boolean avaJ8=true;
		boolean avaJ9=false;
		
		System.out.println(avaJ);
		System.out.println(avaJ1);
		System.out.println(avaJ2);
		System.out.println(avaJ3);
		System.out.println(avaJ4);
		System.out.println(avaJ5);
		System.out.println(avaJ6);
		System.out.println(avaJ7);
		System.out.println(avaJ8);
		System.out.println(avaJ9);
		
	}

}
