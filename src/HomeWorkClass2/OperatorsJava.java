package HomeWorkClass2;

public class OperatorsJava {

	public static void main(String[] args) {
		
		float qw=11.22f;
		double rt=123.123;
		
		double add=qw+rt;
		double subtract=rt-qw;
		double multiply=qw*rt;
		double divide=rt/qw;
		
		System.out.println("The sum of 2 numbers "+qw+" and "+rt+" is equal to "+add+".");
		System.out.println("The subtraction of 2 numbers "+rt+" and "+qw+" is equal to "+subtract+".");
		System.out.println("The multiplication of 2 numbers "+qw+" and "+rt+" is equal to "+multiply);
		System.out.println("The division of 2 numbers "+rt+" and "+qw+" is equal to "+divide);
		

	}

}
